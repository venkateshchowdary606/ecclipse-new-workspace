package com.technoelevate.java_revision.basics;

public class Brain {
	private double weight;
	private int size;
	public Brain(double weight, int size) {
		super();
		this.weight = weight;
		this.size = size;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	

}
