package com.technoelevate.java_revision.basics;
public class Abstraction {
	private int i;
	private int j;
	private int k;
	public Abstraction(int i, int j, int k) {
		super();
		this.i = i;
		this.j = j;
		this.k = k;
	}
	public int getI() {
		
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	public int getK() {
		return k;
	}
	public void setK(int k) {
		this.k = k;
	}
	

}
