package com.technoelevate.java_revision.basics;

public class Heart {
	private double bp;
	private double weight;
	public Heart(double bp, double weight) {
		super();
		this.bp = bp;
		this.weight = weight;
	}
	public double getBp() {
		return bp;
	}
	public void setBp(double bp) {
		this.bp = bp;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	

}
